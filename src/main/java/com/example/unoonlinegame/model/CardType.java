package com.example.unoonlinegame.model;


/**
 * The `CardType` enum represents the different types of Uno cards.
 * Each card type has a specific functionality and behavior in the game.
 *
 * @author Principal Authors:
 * - Laksh Kundnani
 * - Devendra Bisht
 * - Vuong Quoc Nguyen
 * - Neha
 */
public enum CardType {
    /**
     * Represents a regular number card in Uno.
     */
    NUMBER,

    /**
     * Represents a Draw Two card in Uno.
     */
    DRAW_TWO,

    /**
     * Represents a Draw Four card in Uno.
     */
    DRAW_FOUR,

    /**
     * Represents a Reverse card in Uno.
     */
    REVERSE,

    /**
     * Represents a Skip card in Uno.
     */
    SKIP,
    /**
     * Represents a Wild card in Uno
     */
    WILD_CARD
}
