package com.example.unoonlinegame.model;

/**
 * The `Color` enum represents the different types of Uno cards.
 * Each color has a specific functionality and behavior in the game.
 *
 * @author Principal Authors:
 * - Laksh Kundnani
 * - Devendra Bisht
 * - Vuong Quoc Nguyen
 * - Neha
 */
public enum Color {
    /**
     * Represents a blue card in Uno.
     */
    blue,
    /**
     * Represents a red card in Uno.
     */
    red,
    /**
     * Represents a yellow card in Uno.
     */
    yellow,
    /**
     * Represents a green card in Uno.
     */
    green,
    /**
     * Represents a black card in Uno.
     */
    black
}